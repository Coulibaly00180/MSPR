class User {
  String id;
  String pseudo;
  String password;
  String nom;
  String prenom;
  String email;
  String adresse;
  String user_photo;

  User({
    required this.id,
    required this.pseudo,
    required this.password,
    required this.nom,
    required this.prenom,
    required this.email,
    required this.adresse,
    required this.user_photo,
  });
}
